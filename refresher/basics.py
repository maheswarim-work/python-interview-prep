# list
fruits_list = ['apple', 'banana', 'cherry', 'apple']
print(fruits_list)

# tuple
fruits_tuple = ('apple', 'banana', 'cherry', 'apple')
print(fruits_tuple)

# dict
fruits_dict = {
    'a': 'apple',
    'b': 'banana',
    'c': 'cherry'
}
print(fruits_dict)

# set
fruits_set = {'apple', 'banana', 'cherry', 'apple'}
print(fruits_set)
